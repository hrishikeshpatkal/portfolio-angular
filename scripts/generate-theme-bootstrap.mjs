import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.dirname(fileURLToPath(import.meta.url));
const configPath = path.join(root, '../src/app/data/theme-config.ts');
const outputPath = path.join(root, '../public/theme-bootstrap.js');

const source = readFileSync(configPath, 'utf8');

const defaultMatch = source.match(/export const DEFAULT_THEME: ThemeId = '([^']+)'/);
const themesBlock = source.match(/export const PORTFOLIO_THEMES[\s\S]*?\];/);

if (!defaultMatch || !themesBlock) {
  throw new Error('Could not parse theme-config.ts for bootstrap generation.');
}

const defaultTheme = defaultMatch[1];
const themeIds = [...themesBlock[0].matchAll(/id: '([^']+)'/g)].map((match) => match[1]);

const bootstrap = `/* Generated by scripts/generate-theme-bootstrap.mjs — do not edit manually */
(function () {
  var themes = ${JSON.stringify(themeIds)};
  var defaultTheme = ${JSON.stringify(defaultTheme)};

  try {
    var stored = localStorage.getItem('portfolio-theme');
    var theme = themes.indexOf(stored) >= 0 ? stored : defaultTheme;
    document.documentElement.setAttribute('data-theme', theme);
  } catch (error) {
    document.documentElement.setAttribute('data-theme', defaultTheme);
  }
})();
`;

writeFileSync(outputPath, bootstrap, 'utf8');
console.log(`Wrote ${outputPath} (${themeIds.length} themes, default: ${defaultTheme})`);
